void main() {
  // Perform addition operation
  print("Addition: ${add(5, 3)}");

  // Perform multiplication operation
  print("Multiplication: ${multiply(5, 3)}");
}

// Function to perform addition
int add(int a, int b) {
  return a + b;
}

// Function to perform multiplication
int multiply(int a, int b) {
  return a * b;
}

